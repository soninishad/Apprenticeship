// In this we create a package with name mypack and inside this package, we create a class with name MyUtil. 
// In MyUtil class we create two methods add() and greatest. add() method return sum of two numbers
//   and greatest () method return greatest number in two numbers.


package mypack;
public class MyUtil
 {
    public int add(int x , int y)
     {
         return (x+y);
     }
  public int greatest (int x, int y)
    { 
    if (x>y)
    { 
    return x;
   }
  else 
  {
     return y ;
   }

  }
  }







