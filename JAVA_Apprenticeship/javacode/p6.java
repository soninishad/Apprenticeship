//WAP to check given number is even or odd .

import java.util.*;

class p6
{
public static void main(String [] args)
{
int n;
Scanner sc=new Scanner(System.in);
System.out.print("Enter a number : ");
n=sc.nextInt();

if(n%2==0)
{
System.out.println("Even number");
}
else
{
System.out.println("Odd number ");
}

}
} 